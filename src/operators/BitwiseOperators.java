package operators;

public class BitwiseOperators {

	public static void main(String[] args) {
		int x = 5, y = 3;
        System.out.println("Bitwise AND: " + (x & y));
        System.out.println("Bitwise OR: " + (x | y));
        System.out.println("Bitwise XOR: " + (x ^ y)); // how it operates

	}

}

//Answer
/*
Bitwise AND: 1
Bitwise OR: 7
Bitwise XOR: 6
*/